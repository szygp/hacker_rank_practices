def sockMerchant(n, ar):
    # Write your code here
    dict_ar = {}
    pairs = 0
    for i in ar:
        dict_ar[i] = 0
    for i in ar:
        if i in dict_ar:
            dict_ar[i] += 1
    for i in dict_ar:
        pairs += dict_ar[i] // 2
    return pairs


